#include "minitalk.h"

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int n)
{
	long	a;

	a = n;
	if (a < 0)
	{
		a = -1 * a;
		write(1, "-", 1);
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	if (a <= 9)
	{
		ft_putchar(a + 48);
	}
}
